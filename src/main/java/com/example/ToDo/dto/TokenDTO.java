package com.example.ToDo.dto;

public class TokenDTO {
	
	private String token;
	
	
	

	public TokenDTO(String token) {
		super();
		this.token = token;
	}

	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}

	/**
	 * @param token the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}
	
	

}
